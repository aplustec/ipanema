							<h3><a href="#">7. Como é o Processo Seletivo?</a></h3>
							<div>
								As provas serão realizadas todas as segundas, terças e quartas, e as entrevistas serão nas quintas-feiras.<br/><br/>
								As provas são realizadas diretamente no computador, utilizando o banco de conteúdos da Faculdade Ipanema. Serão 25 questões, e o tempo de duração da prova é de 2 horas.<br/><br/>
								A prova é gerada de forma aleatória diretamente deste banco de conteúdos, o que faz com que nenhuma prova seja igual à outra, embora todas tenham exatamente o mesmo grau de dificuldade. A nota é dada na mesma hora e os candidatos aprovados na prova serão encaminhados para a entrevista individual.<br/><br/>
								Uma vez aprovado na prova, o candidato será encaminhado para a entrevista individual.<br/><br/>
								As entrevistas ocorrerão todas as quintas feiras, em horários previamente agendados e com duração de 20 minutos, quando o candidato deverá discorrer, diante de uma banca composta por até 3 pessoas, sobre temas da atualidade. O tema de cada candidato será informado no momento do agendamento da entrevista.
							</div>
