<h2>Empreendedor Pearson</h2>
       <span class="type">Idiomas e Extens&atilde;o</span><br />
<h3>A Faculdade Ipanema, recentemente, firmou uma parceria com a Pearson Education, uma das maiores editoras do mundo e que detêm o direito de um programa de empreendedores, o Empreendedor Pearson. Este programa faz parte, na realidade, da certificação internacional BTEC, certificação esta fornecida pela Pearson Education desde 1973, em parceria com a Universidade de Londres. Esta parceria nos permitirá fornecer GRATUITAMENTE para os nossos alunos, um dos módulos do programa e, automaticamente, uma certificação internacional válida e reconhecida. No nosso ponto de vista, este é um grande diferencial para os alunos que sairão da faculdade diretamente para o mercado de trabalho. Além disto, estamos desenvolvendo projetos internos como a criação de um ambiente de idiomas onde nosso aluno poderá fazer, em paralelo com sua graduação, um curso de idiomas a preço acessível, sem prejudicar seu planejamento financeiro. Todas estas ações visam criar um profissional mais completo para o mercado de trabalho. <br />
  <br />
  <span class="subtitlte">O EMPREENDEDOR</span><br />
  PROGRAMA DE DESENVOLVIMENTO DE HABILIDADES PROFISSIONAIS E DE COMPETÊNCIAS PARA O EMPREENDEDORISMO<br />
  <br />
No mundo contemporâneo, o talento empreendedor assume papel fundamental, o que faz com que os processos educacionais sejam criados com a finalidade de promover o desenvolvimento de competências que possibilitem ao indivíduo atuar de forma cidadã.

<br />
<br />
Atenta às demandas da atualidade a Pearson, desenvolveu um sistema para qualificação profissional conhecido como <strong>BTEC-Business & Technology Education Council.

</strong><br />
<br />
Direcionada pela perspectiva dos BTEC WorkSkills é que a Pearson no Brasil cria um programa de certificação denominado<strong> O Empreendedor,</strong> orientado para o desenvolvimento de competências para o empreendedorismo, por meio do qual instituições educacionais e empresariais prepararam pessoas para a qualificação profissional, promovendo o desenvolvimento de conhecimentos acerca da gestão empresarial e da gestão da carreira profissional.
<br />
<br />
<br />
<span class="subtitlte">OBJETIVOS E DESIGN DO PROGRAMA </span><br />
<br />
Proporcionar condições para o desenvolvimento do talento empreendedor, possibilitando a iniciação para a formação profissional por meio de processos educacionais que promovam a compreensão do mundo do trabalho e das organizações empresariais com vistas à certificação de habilidades inerentes a um profissional competente.

<br />
<br />
Todos os temas foram concebidos e planejados de maneira a assegurar que os conteúdos básicos para o empreendedorismo sejam privilegiados, por meio de eixos articuladores do desenvolvimento de competências necessárias para o entendimento das variáveis inerentes à gestão empresarial: planejamento, mercado e organização.

<br />
<br />
A coleção é composta por cadernos para o professor e para os alunos, que a cada módulo tratam de temas e certificações específicas. Os módulos são independentes e aplicáveis para alunos a partir de 14 anos. 
