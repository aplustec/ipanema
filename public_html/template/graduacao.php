       <h2>Gest&atilde;o Financeira</h2>
       <span class="type">Graduação</span>
       <h3><br />
        O curso de Gestão Financeira tem como objetivo formar profissionais capazes de analisar os movimentos do mercado financeiro e de tomar decisões que potencializem o desempenho da empresa.

        <br />
        <br />
        O profissional formado pela Faculdade Ipanema poderá atuar na área financeira, controladoria e auditoria de empresas, bancos, corretoras de valores, instituições de crédito, organizações do Terceiro Setor ou abrir o seu próprio negócio.

        <br />
        <br />
        Seu dia a dia será envolvido por produção e análise de demonstrativos contábeis e relatórios financeiros, e de indicadores presentes nestes relatórios.

        <br />
        <br />
        <span class="subtitlte">Média de remuneração para profissional recém-formado:
        </span><br />
        Em empresa de grande porte - R$ 3.000,00
        <br />
        Em empresa de pequeno porte - R$ 2.000,00

