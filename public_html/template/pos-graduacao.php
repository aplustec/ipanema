       <h2>MBA em Redes Socias e Inova&ccedil;&atilde;o Digital</h2>
       <span class="type">P&oacute;s-Graduação</span>
       <h3><br />
         <span class="subtitlte">Objetivos:</span><br />
         • Promover a compreensão do fenômeno das redes sociais e seu papel na comunicação, garantindo assim elementos que auxiliem aos profissionais a avaliar, planejar e desenvolver ações utilizando-as;
         <br />
         • Possibilitar o acesso a novos conceitos e procedimentos voltados à inovação digital, por meio da troca de experiências e convívio com profissionais altamente qualificados;
         <br />
         • Promover um ambiente de análise e reflexão crítica sobre práticas e estratégias utilizadas, incentivando o desenvolvimento de soluções criativas e resultados de sucesso;
         <br />
         • Oportunizar a ampliação de conhecimentos para o desenvolvimento e melhoria de procedimentos no trabalho cotidiano, por meio da pesquisa e do trabalho em equipe.

         <br />
         <br />
         <span class="subtitlte">Área de Atuação:
         </span><br />
         Empresas e instituições públicas e privadas.

         <br />
         <br />
         <span class="subtitlte">Público Alvo:
         </span><br />
         Profissionais graduados que atuam ou queiram atuar na área de marketing, publicidade e relações públicas, particularmente ocupando postos de liderança. Profissionais que atuam ou queiram atuar nas áreas de planejamento e inovação tecnológica em empresas e instituições públicas e privadas. Profissionais interessados em conhecer e desenvolver projetos e produtos neste segmento.

<br />
<br />
<span class="subtitlte">Metodologia: </span><br />
Trata-se de um curso presencial, cuja fundamentação está baseada numa visão interdisciplinar do processo de construção de conhecimento. A abordagem dos conteúdos terá como diretriz a estreita correlação teoria-prática, tendo como princípio a reflexão sobre práticas.

<br />
<br />
Também fará parte do curso a oferta de oportunidades para que os participantes possam estar aprofundando seus estudos por meio da pesquisa, outro princípio norteador do curso. A carga horária de cada componente curricular está constituída por uma parte volta ao estudo dos conteúdos próprios da área ou temas a serem desenvolvidos, bem como uma parte destinada a atividades de pesquisa e investigação da prática docente.

<br />
<br />
Ao longo do curso também haverá momentos específicos de orientação para a elaboração e desenvolvimento de projeto de pesquisa que resultará em Trabalho de Conclusão de Curso.

<br />
<br />
<span class="subtitlte">Avaliação:</span><br />
Será considerado aprovado no curso os alunos que tiverem freqüência igual ou superior a 75% do curso, de acordo com o que prevê Resolução CNE/CES nº 1 de 8/6/2007; realizar de forma satisfatória as atividades e tarefas exigidas em cada componente curricular; elaborar e desenvolver projeto de pesquisa sobre tema próprio à área de Redes Sociais e Inovação Digital; elaborar Trabalho de Conclusão de Curso, de acordo com as regras e padrões estabelecidos no curso.</h3>
