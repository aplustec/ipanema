                    <p>
						<b>Justificativa:</b><br/>Apesar dos esforços no sentido de se construir uma escola inclusiva, ainda persistem no cotidiano escolar práticas de caráter excludente, tornando-se urgente que professores e profissionais que atuam na área de educação tenham oportunidades para vivenciar situações de aprendizagem que lhes possibilitem refletir sobre suas ações e criar estratégias que garantam a construção de uma intervenção pedagógica voltada a garantir a aprendizagem e o desenvolvimento de todos os alunos e alunas com os quais trabalha.<br /><br />A partir dos levantamentos realizados regularmente pelo Censo Escolar, tem se verificado um aumento significativo no ingresso e permanência dos alunos no sistema regular de ensino, inclusive crianças com necessidades educacionais especiais. Instaura-se como desafio aos profissionais da educação possibilitar meios para que seja garantido o direito desses alunos à continuidade de estudos e ao acesso a níveis mais elevados de conhecimentos e bens culturais.<br /><br />Neste contexto, assume fundamental importância o atendimento psicopedagógico no sentido de promover intervenção visando a garantia da aprendizagem, por meio de processos e procedimentos que atendam às necessidades individuais e busquem soluções para transtornos e dificuldades de aprendizagem.<br /><br />De acordo com Art. 4º do Projeto de Lei Nº 3.512-C/2008 são atividades e atribuições da Psicopedagogia sem prejuízo do exercício das atividades e atribuições pelos profissionais da educação habilitados:<br />I - intervenção psicopedagógica, visando à solução dos problemas de aprendizagem, tendo por enfoque o indivíduo ou a instituição de ensino público ou privado ou outras instituições onde haja a sistematização do processo de aprendizagem na forma da lei;<br />II – realização de diagnóstico e intervenção psicopedagógica, mediante a utilização de instrumentos e técnicas próprios de Psicopedagogia;<br />III - utilização de métodos, técnicas e instrumentos psicoped