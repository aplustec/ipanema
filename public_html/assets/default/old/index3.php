						<b>Objetivos:</b><br/>• Possibilitar uma compreensão multidisciplinar dos aspectos e processos inerentes à aprendizagem, nos diferentes âmbitos nos quais esta se insere;<br/>• Desenvolver competências e habilidades voltadas à análise, planejamento, orientação e intervenção psicopedagógica individual e institucional;<br/>• Planejar ações preventivas capazes de favorecer a integração aos espaços de aprendizagem formais ou não;<br/><br/>
